package eserciziBase;
/*
 * Esercizio 2
 Scrivere un programma che confronta due numeri interi a e b e stampa se i numeri sono uguali
 oppure no.
 * */
public class Esercizio2 {

	public static void main(String[] args) {
		int a = 2, b = 2;
		if(a == b) {
			System.out.println("I due numeri sono uguali.");
		}else {
			System.out.println("I due numeri sono diversi.");
		}
	}

}
